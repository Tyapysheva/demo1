package com.example.demo.entity;

public interface Worker {

    /**
     * Method returning worker's name
     *
     * @return name Worker's name
     * */
    String name();

    /**
     * Method returning worker's lastname
     *
     * @return lastname Worker's lastname
     * */
    String lastname();

    /**
     * Method returning worker's year of birth
     *
     * @return yearOfBirth Worker's year of birth
     * */
    short yearOfBirth();

    /**
     * Method returning worker's phone
     *
     * @return phone Worker's phone
     * */
    String phone();

}
